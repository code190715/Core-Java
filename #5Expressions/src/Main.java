
public class Main {

	public static void main(String[] args) {
		
		int friends = 10;
		
		friends = friends + 5;
		System.out.println(friends);
		
		friends = friends - 10;
		System.out.println(friends);
		
		friends = friends * 2;
		System.out.println(friends);
		
		friends = friends / 2;
		System.out.println(friends);
		
		friends = friends % 2;
		System.out.println(friends);
		
		friends++;
		System.out.println(friends);
		
		friends--;
		System.out.println(friends);
		
		
		
		double friends1 = 10;
		friends1 = (double) friends1 / 3;
		
		System.out.println(friends1);
	

	}

}



